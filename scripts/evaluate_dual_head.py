"""
Evaluate a trained Phase 0.5 dual-head checkpoint on any MirrorSceneDataset split.

This script is for the cross-domain smoke gate. It does not train. It loads a
dual-head checkpoint and reports whether first-surface and secondary-path heads
still recover both layers on the target split.
"""

