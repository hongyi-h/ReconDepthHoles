"""
Dataset for Layered Pointmap training on synthetic mirror scenes.

Loads scenes generated by blender_mirror_scene.py and produces:
- images: (S, 3, H, W) — multi-view RGB
- batch dict with:
    - world_points: (S, H, W, 3) — first-surface pointmap GT
    - world_points_secondary: (S, H, W, 3) — secondary-path pointmap GT (mirror-visible geometry)
    - point_masks: (S, H, W) — valid first-surface depth mask
    - point_masks_secondary: (S, H, W) — valid secondary-path mask
    - depths: (S, H, W) — first-surface depth
    - depths_secondary: (S, H, W) — secondary-path depth
    - material_masks: (S, H, W) — 0=diffuse, 1=mirror region
    - mirror_planes: (N_mirrors, 4) — plane parameters (nx, ny, nz, d)
    - extrinsics: (S, 4, 4) — camera extrinsics
    - intrinsics: (S, 3, 3) — camera intrinsics

Compatible with VGGT's MultitaskLoss interface (world_points, point_masks, depths).
"""

import os
import json
import glob

import numpy as np
import torch
from torch.utils.data import Dataset
from PIL import Image


class MirrorSceneDataset(Dataset):
    """Dataset for synthetic mirror scenes with layered pointmap GT."""

    def __init__(
        self,
        root_dir: str,
        num_views: int = 10,
        img_size: int = 518,
        split: str = "train",
        max_scenes: int = None,
    ):
        """
        Args:
            root_dir: Path to data/synthetic/{split} containing scene_XXXXX/ dirs
            num_views: Number of views to sample per scene
            img_size: Target image width (VGGT default: 518)
            split: "train" or "val"
            max_scenes: Cap number of scenes (for debugging)
        """
        self.root_dir = root_dir
        self.num_views = num_views
        self.img_size = img_size
        self.split = split

        # Discover all scenes
        self.scene_dirs = sorted(glob.glob(os.path.join(root_dir, "scene_*")))
        # Filter to scenes that have scene_meta.json (complete generation)
        self.scene_dirs = [
            d for d in self.scene_dirs
            if os.path.isfile(os.path.join(d, "scene_meta.json"))
        ]

        if max_scenes is not None:
            self.scene_dirs = self.scene_dirs[:max_scenes]

        print(f"[MirrorSceneDataset] Found {len(self.scene_dirs)} scenes in {root_dir}")

    def __len__(self):
        return len(self.scene_dirs)

    def __getitem__(self, idx):
        scene_dir = self.scene_dirs[idx]

        # Load scene metadata
        with open(os.path.join(scene_dir, "scene_meta.json")) as f:
            meta = json.load(f)

        total_views = meta["num_views"]
        H, W = meta["resolution"]

        # Sample views (random subset if more views than needed)
        if total_views > self.num_views:
            view_ids = np.random.choice(total_views, self.num_views, replace=False)
            view_ids.sort()
        else:
            view_ids = np.arange(total_views)

        # Load per-view data
        images = []
        depths = []
        depths_secondary = []
        extrinsics = []
        intrinsics = []

        for vid in view_ids:
            # RGB image
            rgb_path = os.path.join(scene_dir, f"rgb_{vid:03d}.png")
            img = np.array(Image.open(rgb_path).convert("RGB"))  # (H, W, 3)
            images.append(img)

            # First-surface depth from EXR
            depth_first = self._load_exr_depth(
                os.path.join(scene_dir, f"render_{vid:03d}.exr"), H, W
            )
            depths.append(depth_first)

            # Secondary-path depth (from first mirror — extend for multi-mirror later)
            secondary_path = os.path.join(scene_dir, f"secondary_{vid:03d}_mirror00.exr")
            if os.path.isfile(secondary_path):
                depth_second = self._load_exr_depth(secondary_path, H, W)
            else:
                depth_second = np.zeros((H, W), dtype=np.float32)
            depths_secondary.append(depth_second)

            # Camera matrices
            cam_data = np.load(os.path.join(scene_dir, f"camera_{vid:03d}.npz"))
            extrinsics.append(cam_data["extrinsic"])
            intrinsics.append(cam_data["intrinsic"])

        # Stack into arrays
        images = np.stack(images)  # (S, H, W, 3)
        depths = np.stack(depths)  # (S, H, W)
        depths_secondary = np.stack(depths_secondary)  # (S, H, W)
        extrinsics = np.stack(extrinsics)  # (S, 4, 4)
        intrinsics = np.stack(intrinsics)  # (S, 3, 3)

        # Compute world-space pointmaps from depth + camera
        world_points = self._depths_to_pointmaps(depths, extrinsics, intrinsics)
        world_points_secondary = self._depths_to_pointmaps(
            depths_secondary, extrinsics, intrinsics
        )

        # Masks: valid depth > 0 and < some max (Blender uses 1e10 for sky)
        MAX_DEPTH = 100.0
        point_masks = (depths > 0) & (depths < MAX_DEPTH)
        point_masks_secondary = (depths_secondary > 0) & (depths_secondary < MAX_DEPTH)

        # Mirror planes
        mirror_planes_path = os.path.join(scene_dir, "mirror_planes.npy")
        if os.path.isfile(mirror_planes_path):
            mirror_planes = np.load(mirror_planes_path)  # (N, 4)
        else:
            mirror_planes = np.zeros((0, 4), dtype=np.float32)

        # Resize if needed (skip if already close to target — within 20%)
        if abs(W - self.img_size) / self.img_size > 0.2:
            images, depths, depths_secondary, \
                point_masks, point_masks_secondary, intrinsics = self._resize_all(
                    images, depths, depths_secondary,
                    point_masks, point_masks_secondary, intrinsics, self.img_size
                )
            # Recompute pointmaps from resized depths
            world_points = self._depths_to_pointmaps(depths, extrinsics, intrinsics)
            world_points_secondary = self._depths_to_pointmaps(
                depths_secondary, extrinsics, intrinsics
            )

        # Convert to tensors
        # Images: (S, H, W, 3) -> (S, 3, H, W), normalized to [0, 1]
        images_t = torch.from_numpy(images).permute(0, 3, 1, 2).float() / 255.0

        batch = {
            "images": images_t,
            "world_points": torch.from_numpy(world_points).float(),
            "world_points_secondary": torch.from_numpy(world_points_secondary).float(),
            "point_masks": torch.from_numpy(point_masks).bool(),
            "point_masks_secondary": torch.from_numpy(point_masks_secondary).bool(),
            "depths": torch.from_numpy(depths).float(),
            "depths_secondary": torch.from_numpy(depths_secondary).float(),
            "extrinsics": torch.from_numpy(extrinsics).float(),
            "intrinsics": torch.from_numpy(intrinsics).float(),
            "mirror_planes": torch.from_numpy(mirror_planes).float(),
            "scene_dir": scene_dir,
        }

        return batch

    def _load_exr_depth(self, path, H, W):
        """Load depth from EXR file. Falls back to zeros if not available."""
        try:
            import OpenEXR
            import Imath

            exr_file = OpenEXR.InputFile(path)
            header = exr_file.header()
            dw = header["dataWindow"]
            exr_w = dw.max.x - dw.min.x + 1
            exr_h = dw.max.y - dw.min.y + 1

            # Try to read Z channel (depth pass)
            float_type = Imath.PixelType(Imath.PixelType.FLOAT)
            channels = header["channels"]

            if "View Layer.Depth.Z" in channels:
                z_str = exr_file.channel("View Layer.Depth.Z", float_type)
            elif "Z" in channels:
                z_str = exr_file.channel("Z", float_type)
            elif "depth" in channels:
                z_str = exr_file.channel("depth", float_type)
            else:
                # Fall back: use first available channel
                ch_name = list(channels.keys())[0]
                z_str = exr_file.channel(ch_name, float_type)

            depth = np.frombuffer(z_str, dtype=np.float32).reshape(exr_h, exr_w)

            # Resize if needed
            if (exr_h, exr_w) != (H, W):
                from PIL import Image as PILImage
                depth_img = PILImage.fromarray(depth, mode='F')
                depth_img = depth_img.resize((W, H), PILImage.NEAREST)
                depth = np.array(depth_img)

            return depth

        except ImportError:
            # OpenEXR not available — return zeros
            print(f"WARNING: OpenEXR not installed, cannot load {path}")
            return np.zeros((H, W), dtype=np.float32)
        except Exception as e:
            print(f"WARNING: Failed to load {path}: {e}")
            return np.zeros((H, W), dtype=np.float32)

    def _depths_to_pointmaps(self, depths, extrinsics, intrinsics):
        """Convert depth maps to world-space pointmaps.

        Args:
            depths: (S, H, W)
            extrinsics: (S, 4, 4) — world-to-camera
            intrinsics: (S, 3, 3)

        Returns:
            pointmaps: (S, H, W, 3) — world coordinates
        """
        S, H, W = depths.shape
        pointmaps = np.zeros((S, H, W, 3), dtype=np.float32)

        for s in range(S):
            K = intrinsics[s]
            ext = extrinsics[s]
            depth = depths[s]

            fx, fy = K[0, 0], K[1, 1]
            cx, cy = K[0, 2], K[1, 2]

            u, v = np.meshgrid(np.arange(W), np.arange(H))
            x_cam = (u - cx) * depth / fx
            y_cam = (v - cy) * depth / fy
            z_cam = depth

            # Stack to (H, W, 4) homogeneous camera coords
            ones = np.ones_like(depth)
            pts_cam = np.stack([x_cam, y_cam, z_cam, ones], axis=-1)

            # Camera-to-world
            cam_to_world = np.linalg.inv(ext)
            pts_world = (cam_to_world @ pts_cam.reshape(-1, 4).T).T.reshape(H, W, 4)
            pointmaps[s] = pts_world[:, :, :3]

        return pointmaps

    def _resize_all(self, images, depths, depths_secondary,
                    point_masks, point_masks_secondary,
                    intrinsics, target_w):
        """Resize arrays to target width, maintaining aspect ratio. Returns updated intrinsics."""
        S, H, W, _ = images.shape
        scale = target_w / W
        new_W = target_w
        new_H = int(H * scale)
        # Make divisible by 14 (VGGT patch size)
        new_H = (new_H // 14) * 14
        new_W = (new_W // 14) * 14

        from PIL import Image as PILImage

        def resize_images(imgs):
            out = []
            for img in imgs:
                pil_img = PILImage.fromarray(img)
                pil_img = pil_img.resize((new_W, new_H), PILImage.BILINEAR)
                out.append(np.array(pil_img))
            return np.stack(out)

        def resize_depth(d_arr):
            out = []
            for d in d_arr:
                pil_d = PILImage.fromarray(d, mode='F')
                pil_d = pil_d.resize((new_W, new_H), PILImage.NEAREST)
                out.append(np.array(pil_d))
            return np.stack(out)

        def resize_masks(m_arr):
            out = []
            for m in m_arr:
                pil_m = PILImage.fromarray(m.astype(np.uint8) * 255)
                pil_m = pil_m.resize((new_W, new_H), PILImage.NEAREST)
                out.append(np.array(pil_m) > 127)
            return np.stack(out)

        images = resize_images(images)
        depths = resize_depth(depths)
        depths_secondary = resize_depth(depths_secondary)
        point_masks = resize_masks(point_masks)
        point_masks_secondary = resize_masks(point_masks_secondary)

        # Update intrinsics for resize
        scale_x = new_W / W
        scale_y = new_H / H
        intrinsics = intrinsics.copy()
        intrinsics[:, 0, :] *= scale_x
        intrinsics[:, 1, :] *= scale_y

        return (images, depths, depths_secondary,
                point_masks, point_masks_secondary, intrinsics)


def collate_fn(batch_list):
    """Custom collate that handles variable mirror_planes and string fields."""
    batch = {}
    # Tensor fields: stack along batch dim
    tensor_keys = [
        "images", "world_points", "world_points_secondary",
        "point_masks", "point_masks_secondary",
        "depths", "depths_secondary", "extrinsics", "intrinsics",
    ]
    for key in tensor_keys:
        batch[key] = torch.stack([b[key] for b in batch_list])

    # Variable-length fields: keep as list
    batch["mirror_planes"] = [b["mirror_planes"] for b in batch_list]
    batch["scene_dir"] = [b["scene_dir"] for b in batch_list]

    return batch
